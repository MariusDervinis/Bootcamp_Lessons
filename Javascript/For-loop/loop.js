// printing each character in a string with a for loop, creating temporary var = i
var str ="hello";
for(var i = 0; i < str.length; i++) {
console.log(str[i]);
}

// with a while loop, we have to create a new var = count
var str = "hello";
var count = 0;

while(count < str.length) {
    console.log(str[count]);
    count++;
}
